	SUBROUTINE DISTDG(FI1,LAM1,FI2,LAM2,DIST)
	REAL LAM1,LAM2,LAMBDA
	DATA R0/6378.245/,PI/3.1415926/
	LAMBDA=(LAM1+LAM2)/2.
	R=R0*(1.-0.00335*SIN(LAMBDA*PI/180.)**2)
	a1=lam1*pi/180.
	a2=lam2*pi/180.
	dfi=(fi2-fi1)*pi/180.
	DRR=SQRT(2.*(1.-cos(a1)*cos(a2)*cos(dfi)-sin(a1)*sin(a2)))
	ANGL=ASIN(DRR/2.)
	DIST=R*ABS(ANGL)/5.
	RETURN
	END