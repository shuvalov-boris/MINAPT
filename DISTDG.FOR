C*   х явхрюъ гелкч ьюпнл я онксвеммшл пюдхсянл пюяявхршбюеряъ
C*   пюяярнъмхе лефдс рнвйюлх он ятепе.
C*  рНВМНЯРЭ ХГ-ГЮ НЯpЕДМЕМХЪ pЮДХСЯЮ: pЮЯЯРНЪМХЕ l=lam2-lam1=4ЦpЮД.
C*   МЮ ОНКЧЯЕ ДЮЕР НЬХАЙС 1 ЙЛ; Я СЛЕМЭЬЕМХЕЛ ЬХpНРШ ЩРЮ НЬХАЙЮ
